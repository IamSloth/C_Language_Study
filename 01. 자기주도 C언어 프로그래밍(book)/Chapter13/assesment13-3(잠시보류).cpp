/** 문제 **
자연수 N과 M을 입력받아서 주사위를 N 번 던져서 나온 눈의 합이 M이 나올 수 있는
모든 경우를 출력하는 프로그램을 작성하시오.

입력 예) 3 10

출력 예) 1 3 6
		 1 4 5
		 1 5 4
		 1 6 3
		 2 2 6
		 2 3 5
		 …
		 6 2 2
		 6 3 1

*/

