#include <stdio.h>

int sum[10];
int a = 0; // 0 -> 1회차, 1 -> 2회차, 2 -> 3회차


void dice(int n, int m)
{
	int i, j, k;
	int s = 0;

	for (i = 1; i <= 6; i++) //주사위를 6번 던지겠습니다.
	{
		sum[a] = i;			 //a회차 주사위 결과값
		if (a == n - 1)		 // 정해진 던진 횟수를 만족하면 (배열이라 1을 뺌)
		{
			s = 0;			 // 그간 던진 값들을 전부 더해봄
			for (j = 0; j <= a; ++j)
				s += sum[j];
			if (s == m)		 // 만약 다 더한값이 원하는 값이면 출력
			{
				for (k = 0; k < n; ++k)
					printf("%d ", sum[k]);
				printf("\n");

			}
		}

		else
		{
			a++;		//회차수 증가
			dice(n, m); //다음회차수 뺑뺑이
			a--;		//다했으면 전회차수로 돌아옴
		}

	}

}

int main()
{
	int n, m;
	scanf("%d %d", &n, &m);
	dice(n, m);
	return 0;
}
